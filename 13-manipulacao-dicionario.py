user = {'name': 'Israel', 'email': 'rael@gmail.com', 6: 'abc'}

print(user) #retorna todo o dicionario
print(list(user)) #retorna apenas as chaves 
print(user.get('name')) #retorna o valor especifico
# user.clear() #limpa o dicionario
print(user.items()) #retorna a lista de chaves e valores
print(user.keys()) #retorna apenas a lista com as chaves
print(user.values()) #retorna apenas os valores das chaves
